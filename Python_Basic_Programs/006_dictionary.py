#1. Create a Dictionary

fruits = {"mango" : 56, "orange" : 58}
print(fruits)

#2. Access an element in the dictionary

print(fruits["mango"])

#3. Update an element in the dictionary

fruits["mango"] = 100
print(fruits)

#4. Add an element in the dictionary

fruits["lemon"] = 15
print(fruits)

#5. delete an element in the dictionary

fruits.pop("lemon")
print(fruits)